L = inp.split("\n")

C_BRACKETS = (")", "]", "}", ">")
O_BRACKETS = ("(", "[", "{", "<")
POINTS = (1, 2, 3, 4)


B = []
for line in L:
    Q = []
    running = True
    for x in line:
        if x in O_BRACKETS:
            Q.append(x)
        else:
            i = C_BRACKETS.index(x)
            open = O_BRACKETS[i]
            if len(Q) == 0:
                running = False
                break
            if Q[-1] == open:
                Q.pop()
            else:
                running = False
                break
    if len(Q) != 0 and running:
        B.append(Q)

SCORES = []
for line in B:
    points = 0
    for x in range(len(line) - 1, -1, -1):
        open = line[x]
        i = O_BRACKETS.index(open)
        points *= 5
        points += POINTS[i]
    SCORES.append(points)

SCORES.sort()
length = len(SCORES)
print(SCORES[int((length - 1) / 2)])
